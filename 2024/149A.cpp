void solve() {
   
   int k = 0;
   cin >> k;

   vector<int> a(12, 0);
   cin >> a;
   dbg(k, a);

   sort(a.begin(), a.end(), [](const auto& x1, const auto& x2) {
      return x1 > x2;
   });
   

   int nowSum = 0;
   int cnt = 0;
   for (int i = 0; i < ssize(a); i++) {
      if (nowSum >= k) {
         println(cnt);
         return;
      } else {
         nowSum += a[i];
         cnt += 1;
      }
   }
   
   return;
}

